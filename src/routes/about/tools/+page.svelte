  <div><h1>Tools</h1></div>
  <div id="section1" class="uk-section">
    <h3>Why We Switched From Amazon Amplify to Supabase:</h3>

    <p>
      Amazon Amplify provided a comprehensive set of tools for mobile and web applications but was complex and potentially excessive for our needs.
    </p>

    <p>
      Supabase, an open-source alternative, offered essential services like database, authentication, and real-time capabilities in a simpler, more cost-effective package.
    </p>

    <p>
      Our Reasoning: We encountered technical challenges with Amplify's complexity and cost. Supabase offered a streamlined and efficient solution better suited to the scope of our project.
    </p>
  </div>

  <div id="section2" class="uk-section">
    <h3>SvelteKit</h3>

    <p>
      React, while a robust library for user interfaces, presented us with
      issues in larger bundle sizes and performance.
    </p>

    <p>
      SvelteKit, building on Svelte's compiler-based approach, provides faster
      performance and smaller bundles
    </p>
  </div>

  <div id="section3" class="uk-section">
    <h3>Our Move to Vercel for Deployment:</h3>

    <p>
      Vercel is known for its simplicity in deployment, particularly for JavaScript frameworks like SvelteKit.
    </p>

    <p>
      Our Reasoning: Vercel enhanced our deployment process, offering a seamless transition from development to production. Its compatibility with SvelteKit was a key factor in our choice.
    </p>
  </div>
  <div id="section4" class="uk-section">
    <h3>Conclusion:</h3>

    <p>
      Addressing Technical Difficulties: Our initial technology stack presented challenges in scalability, performance, and complexity.
    </p>

    <p>
      Improved File Management: The new stack allows for more efficient handling of files and deployments.
    </p>
    <p>
      Enhanced Developer Experience: We now enjoy a more intuitive and faster development process.
    </p>
    <p>
      This strategic shift in our technology stack is aligned with our project's evolving needs, promising better performance, simplicity in development, and cost efficiency.
    </p>
